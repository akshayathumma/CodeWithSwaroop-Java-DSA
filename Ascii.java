public class Ascii {
    public static void main(String[] args) {
        int i=66;
        char ch='A';
        System.out.println((char)i);//type conversion
        System.out.println((int)ch);//type conversion
        int n=65;
        char chr='a';
        System.out.println((char)n);
        System.out.println((char)(n+1));//type conversion
        System.out.println((int)chr);//type conversion
    }
}
